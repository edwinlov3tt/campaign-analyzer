// Helper function to get tables for a tactic
export function getTablesForTactic(tactic: string): string[] {
  // Direct match
  if (TACTIC_TABLES[tactic]) {
    return TACTIC_TABLES[tactic];
  }
  
  // Case-insensitive match
  const tacticLower = tactic.toLowerCase();
  for (const [key, value] of Object.entries(TACTIC_TABLES)) {
    if (key.toLowerCase() === tacticLower) {
      return value;
    }
  }
  
  // Partial match
  for (const [key, value] of Object.entries(TACTIC_TABLES)) {
    if (key.toLowerCase().includes(tacticLower) || tacticLower.includes(key.toLowerCase())) {
      return value;
    }
  }
  
  // Default fallback
  return ['Monthly Performance', 'Campaign Performance', 'Creative Performance'];
}